class Command(BaseCommand):
    help = "Définit le boundary (GeoJSON) d'un jardin par nom."

    def add_arguments(self, parser):
        parser.add_argument(
            "name",
            nargs="?",
            default="Jardins Du Mon Caprice",
            help="Nom du jardin (défaut: Jardins Du Mon Caprice)",
        )
        parser.add_argument(
            "--geojson",
            type=str,
            default=None,
            help="GeoJSON en JSON (optionnel; sinon utilise le boundary Mon Caprice intégré)",
        )

    def handle(self, *args, **options):
        name = options["name"]
        geojson_str = options.get("geojson")
        if geojson_str:
            try:
                boundary = json.loads(geojson_str)
            except json.JSONDecodeError as e:
                self.stderr.write(self.style.ERROR(f"GeoJSON invalide: {e}"))
                return
        else:
            boundary = BOUNDARY_MON_CAPRICE

        garden = Garden.objects.filter(nom__iexact=name).first()
        if not garden:
            self.stderr.write(self.style.ERROR(f"Jardin introuvable: {name}"))
            return
        garden.boundary = boundary
        garden.save(update_fields=["boundary"])
        self.stdout.write(self.style.SUCCESS(f"Boundary mis à jour pour: {garden.nom} (id={garden.id})"))
